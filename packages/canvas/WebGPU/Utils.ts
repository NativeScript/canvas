export function parseVertexFormat(value: string) {
	switch (value) {
		case 'uint8x2':
			return 0;
		case 'uint8x4':
			return 1;
		case 'sint8x2':
			return 2;
		case 'sint8x4':
			return 3;
		case 'unorm8x2':
			return 4;
		case 'unorm8x4':
			return 5;
		case 'snorm8x2':
			return 6;
		case 'snorm8x4':
			return 7;
		case 'uint16x2':
			return 8;
		case 'uint16x4':
			return 9;
		case 'sint16x2':
			return 10;
		case 'sint16x4':
			return 11;
		case 'unorm16x2':
			return 12;
		case 'unorm16x4':
			return 13;
		case 'snorm16x2':
			return 14;
		case 'snorm16x4':
			return 15;
		case 'float16x2':
			return 16;
		case 'float16x4':
			return 17;
		case 'float32':
			return 18;
		case 'float32x2':
			return 19;
		case 'float32x3':
			return 20;
		case 'float32x4':
			return 21;
		case 'uint32':
			return 22;
		case 'uint32x2':
			return 23;
		case 'uint32x3':
			return 24;
		case 'uint32x4':
			return 25;
		case 'sint32':
			return 26;
		case 'sint32x2':
			return 27;
		case 'sint32x3':
			return 28;
		case 'sint32x4':
			return 29;
		case 'float64':
			return 30;
		case 'float64x2':
			return 31;
		case 'float64x3':
			return 32;
		case 'float64x4':
			return 33;
		case 'unorm10-10-10-2':
			return 34;
	}
}
